O CILB iniciou seus trabalhos sob a direção da Professora Sandra,<br> depois pela Professora Maria Danizete e, atualmente, pela gestão das Professoras<br><h4> Cássia Lima (Diretora) e Izabel Guimarães (Vice-Diretora).</h4><br> 
